        <h5>Hundred Islands</h5>
        <p>Hundred Islands National Park is a protected area in the province of Pangasinan, Philippines, composed of 124 islands and islets stretching over 16.76 square kilometers. It is a popular tourist destination for its crystal-clear waters, coral reefs, and white-sand beaches. Whether you are looking for a relaxing getaway or an adventure-filled trip, Hundred Islands National Park has something to offer for everyone.</p>
         <p>Visitors can explore the park by island hopping, which usually includes the seven major islands: Governors Island, Virgin Island, Quezon Island, Marcos Island, Scout Island, Children’s Island, and the Cuenco Cave. Each island has unique charm and attractions, such as snorkeling, kayaking, and cliff jumping. The park is also home to various wildlife and fishes, making it a perfect spot for nature lovers and photographers.</p>
