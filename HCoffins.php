        <h5>Why the coffins in Sagada are hanging?</h5>
        <p>One of the most common beliefs behind hanging coffins of Sagada is that moving the bodies of the dead higher up brings them closer to their ancestral spirits. Moreover, people were afraid to be burried in the ground as the dogs would destroy the body and the wet soil would make it quickly decompose. They wanted a place where their corpse would be safe.</p>
        <p>Another reason for hanging coffins of Sagada high up was risk of head hunters. Years ago during the headhunting days, enemies from a different province would hunt for their heads, and take them home as a trophy.</p>    
