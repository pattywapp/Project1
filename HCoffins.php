            <h5>Sagada’s hanging coffins today</h5>
            <p>The interesting part is that the tradition of hanging coffins in Sagada is still alife, but not on the same scale as before. The last funeral like that was in 2010, and the person was Christian. It’s fascinating how the ancient tradition blands with Christianity brought by the colonizers from Spain.</p>
            <p>Nowadays, fewer elders want to be buried in a traditional way. As they are Christian, they want to be able to visit their relatives tombs in the All Saints Day. With Sagada’s hanging coffins is quite difficult to climb up and put the candle. Therefore, the tradition is disappearing.</p>
