            <h5>How to get to the hanging coffins of Sagada?</h5>
            <p>Sagada is a little village and very touristic place as it’s near the ancient terraces in Banaue. To see the hanging coffins of Sagada, you need to come to the Echo Valley. From tourist information turn right to get to the church. The entrance to the Echo Valley is just behind it. You will have to pay for the guide, as the tourism municipality requires. The guide costs 500 pesos so you better come with a group or join one to share the costs.</p>
            <p>Don’t miss seeing the hanging coffins on the way to Sumaguing Cave, which you can spot from the road. The original coffins are on the way to Sumaguing Cave, visible on the left side, on the rocks. Those are the first hanging coffins of Sagada and you can even see the joint hanging down from one of them. The coffins are on the other side of the valley, so you need a good camera zoom or binocular.</p>
            <p>Third place where you can spot the coffins, is when going to Lumiang cave which is connected with Sumaguing cave. As Sagada hanging coffins are spread in the whole valley, you will have a chance to see more of them when getting to the cave.</p> 
