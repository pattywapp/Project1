            <h5>Exploring Fort Santiago</h5>
            <p>Standing in front of the Fort, you start to appreciate the work and skill that went into the building. Yes, like any good castle, Fort Santiago has huge stone walls surrounding it, a river on two sides, and a moat on a third. The walls are six-odd meters tall and are two and a half meters thick and made with large stone blocks.</p>
            <p>Take care when walking around the site because it has numerous caverns and caves where people could quite easily get lost or injured.
