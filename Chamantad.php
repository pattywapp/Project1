            <h5>Chamantad-Tiñan </h5>
         <p>The Chamantad-Tiñan ViewpointIt's renowned for its curvy grass-carpeted scenery. The rolling hills conquering the area come in a variety of
             low and high slopes giving visitors different perspectives. The highest slope hilltop provides the best view of Chamantad Cove. 
             A short walk here will take you to the seascape lined with powdery sand and small to medium-sized stones.</p>
             <p>Before reaching the hilltop, there are plenty of huts along the highway offering a sample of their local
                 sugarcane wine and Ivatan wild tea called tubho. </p>
                 <p>Including sightseeing and relaxing in the mountains, you can also find locals, known as Ivatans,
                     roaming around to borrow their traditional clothes–the Vakul and Kanayi, for photography purposes. 
                     Vakul is their native headgear for sun and rain protection, while Kanayi is a sleeveless jacket they 
                     usually wear when farming.</p>
