            <h5>How to get to Little Boracay</h5>

            <p>
            Unlike Boracay which is famous for its long white beach, Little Boracay is in the middle of the sea. 
            You will need to get a floating house or a floating cottage to get there. Your floating house or cottage will be pulled by a boat to your destination.
            </p>
            <p>
            Via floating cottage<br><br>A floating cottage can take 10 pax. Price ranges from 3,500 to 4,500. Children below 3 years of age are 
            free of charge. It can accommodate up to 15 pax with an additional 400 pesos per head. Strictly not more than 15 persons per cottage.<br><br>
            Via floating house<br><br>
            A floating house can be a little bit more expensive than a floating cottage but during lean season, a floating house can cost just the same as a floating cottage for big groups.
            It can accommodate more than 15 pax per house. A floating house price can vary depending on your provider and the amenities provided. 
            Price ranges from 6,500 per house for 15pax. Additional pax is 300-400.
