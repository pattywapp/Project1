            <h5>What is the Hinatuan Enchanted River?</h5>

            <p>
            Tucked in between the boundaries of two barangays, Cambatong and Talisay, at 14 kilometers away from the town of Hinatuan, Surigao del Sur, 
            the Hinatuan Enchanted River is a saltwater river which directly links with the Pacific Ocean. However, with a length that shortens in comparison
             to other rivers, the “Enchanted River’s” moniker as a “river” is disputed.
         </p>
            <p>
            To date, scientists are still puzzled where the waters of the Hinatuan Enchanted River are coming from and whose mystery is further fueled by the myths and 
            legends of the river. One prominent folklore of the natives includes the idea that mermaids and other cryptids are overseers of the “river.”
            </p>
