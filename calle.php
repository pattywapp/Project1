            <h5>Calle Crisologo</h5>

            <p>Though only 500 meters long, Calle Crisologo in Vigan, Ilocos Sur, is one of the most beautiful streets in the Philippines. 
                It boasts centuries-old stone houses, lovely tungsten lamps, and antique cobblestone, where horse-drawn carriages or kalesas 
                are still used for transport. In fact, the street is a pedestrian-only zone, save for kalesas favored for touring the historical 
                sites around town.<br>
<br> A few of the ancestral houses have also become restaurants that serve the famous Ilocos bagnet (deep fried pork belly) or empanadas. 
Meanwhile, others have been converted into inns and souvenir shops for traditional Inabel linen.
