            <h5>How To Get To Las Cabanas Beach</h5>

    
            <p>Las Cabanas Beach is located around 5km from El Nido main town.

                The best option is to grab a tricycle from El Nido town centre which will take around 15 minutes.

                We paid 150 pesos between three people for a one way trip ($1 USD per person).<br><br>

                There is now a pick up and drop off point specifically for trikes. They will drop you outside of Vanilla Beach shopping centre and you can walk through to the beach.

                Then simply head back to the same point when you’re ready to grab a lift back to town.
                <br><br>

                If you want to make your own way there by scooter, you can rent one from El Nido town from around 500 pesos per day.</p>
           
